package kar.method.defragmenter.views;

public class MethodDescribingItem {
	private String nodeType;
	private String lines;
	private String cocp;
	private String ncocp2;
	
	
	public MethodDescribingItem(){
		
	}
	
	public String getNodeType() {
		return nodeType;
	}
	
	public void setNodeType(String nodeType) {
		this.nodeType = nodeType;
	}
	
	public String getLines() {
		return lines;
	}
	
	public void setLines(String lines) {
		this.lines = lines;
	}
	
	public String getCocp() {
		return cocp;
	}
	
	public void setCocp(String cocp) {
		this.cocp = cocp;
	}
	
	public String getNcocp2() {
		return ncocp2;
	}
	
	public void setNcocp2(String ncocp2) {
		this.ncocp2 = ncocp2;
	}
	
	
}
